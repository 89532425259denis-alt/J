# -*- coding: utf-8 -*-
"""
gost_prompts.py — усиленный промпт генерации под ГОСТ 7.32-2017.

Drop-in замена внутреннего strict_prompt:
    from gost_prompts import strict_prompt_v2 as strict_prompt

Самодостаточен (не зависит от внутренних функций бота). Сигнатура совместима:
    strict_prompt_v2(task, chars, writing_style="classic", doc_type="")
"""
from __future__ import annotations

_STYLES = {
    "classic":    "академический научный стиль, нейтральная лексика, третье лицо",
    "scientific": "строгий научный стиль с терминологией, без публицистики",
    "simple":     "ясный научный стиль, короткие предложения, без канцелярита",
    "popular":    "научно-популярный, но без разговорных оборотов",
}

# Единые правила оформления по ГОСТ 7.32-2017 — выводятся в формат,
# который понимает рендер DOCX (таблицы pipe, формулы $$...$$).
GOST_FORMAT_RULES = (
    "ПРАВИЛА ОФОРМЛЕНИЯ (ГОСТ 7.32-2017):\n"
    "1. Пиши сплошным связным текстом, абзацы по 4–8 предложений.\n"
    "2. ЗАПРЕЩЕНО в тексте абзацев: markdown (#, *, **, ##), эмодзи, "
    "вводные фразы вроде «Конечно», «Вот», «Ниже представлено».\n"
    "3. ТАБЛИЦЫ: только для реально табличных данных (сравнение, классификация, "
    "числовые показатели). Формат: отдельная строка «Таблица — краткое название», "
    "затем | Колонка 1 | Колонка 2 | и обязательный разделитель | --- | --- |. "
    "Номер таблицы НЕ ставь — его проставит система.\n"
    "4. ФОРМУЛЫ: каждую выноси отдельной строкой как $$ формула $$ (пустые строки "
    "до и после). В тексте ссылайся «в формуле (1)». Номер проставит система.\n"
    "5. ЦИТИРОВАНИЕ: ссылки на источники в квадратных скобках [1] или [3, с. 45].\n"
    "6. Не дублируй содержимое таблиц/формул обычным текстом.\n"
    "7. Числа, термины и факты — корректные и проверяемые; без «воды» и повторов."
)


def gost_rules() -> str:
    """Возвращает блок правил ГОСТ — можно подмешать в любой промпт."""
    return GOST_FORMAT_RULES


def strict_prompt_v2(task: str, chars: int,
                     writing_style: str = "classic",
                     doc_type: str = "") -> str:
    style = _STYLES.get(writing_style, _STYLES["classic"])
    lo = int(chars * 0.90)
    hi = int(chars * 1.05)
    dt = f" ({doc_type})" if doc_type else ""
    return (
        f"ЗАДАЧА{dt}: {task}\n\n"
        f"СТИЛЬ: {style}.\n\n"
        f"ОБЪЁМ: строго {lo}–{hi} символов (целевое {chars}). "
        f"Не обрывай мысль на полуслове, заверши абзац.\n\n"
        f"{GOST_FORMAT_RULES}\n\n"
        f"Верни ТОЛЬКО готовый текст без пояснений и без повторения задания."
    )


# Совместимость: если кто-то импортирует strict_prompt из этого модуля
strict_prompt = strict_prompt_v2

if __name__ == "__main__":
    print(strict_prompt_v2("Раскрыть тему влияния политики на экономику", 4000, "classic", "реферат"))

def gost_text_rules_prompt(doc_type: str = "", include_images: bool = False) -> str:
    """Возвращает строгий ГОСТ-блок для вставки в промпт генерации текста.
    include_images=True добавляет правила оформления рисунков (п.6.5)."""
    parts = [_COMMON_TEXT_RULES]
    if doc_type == "esse":
        parts.append(_ESSAY_RULES)
    elif doc_type == "article":
        parts.append(_ARTICLE_RULES)
        parts.append(_STRUCTURE_RULES)
    elif doc_type in ("doklad",):
        parts.append(_STRUCTURE_RULES)
    else:
        parts.append(_STRUCTURE_RULES)
    if include_images:
        parts.append(_FIGURE_RULES)
    return "\n".join(parts)


def gost_figures_rules_prompt() -> str:
    """Строгий блок правил рисунков для промпта (ГОСТ 7.32-2017 п.6.5)."""
    return _FIGURE_RULES


def gost_bibliography_rules_prompt() -> str:
    """Строгий блок для промпта генерации списка литературы."""
    examples = "\n".join(f"  Эталон ({k}): {v}" for k, v in BIB_EXAMPLES_7_0_100.items())
    return _BIB_RULES + "\nЭТАЛОННЫЕ ОБРАЗЦЫ ЗАПИСЕЙ:\n" + examples


def gost_intro_checklist() -> list[str]:
    """Чек-лист обязательных элементов введения (для валидатора)."""
    return ["актуальност", "цель", "задач", "объект", "предмет", "метод"]


def words_for_chars(chars: int) -> int:
    """LLM плохо считают символы, но неплохо — слова.
    Средняя длина русского слова с пробелом ≈ 7.2 символа."""
    return max(30, int(round(chars / 7.2)))
